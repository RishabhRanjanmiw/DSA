#include <iostream>
using namespace std;

int main() {
    char ch = '1';

    cout<<endl;

    switch(ch) {
        case 1: cout << "First" << endl;
        break;
        case 2: cout<< "Second" << endl;
        break;
        default: cout<< "It is not a ch" << endl;
    }


cout<< endl;
}